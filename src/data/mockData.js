export const YEARS = [2020, 2021, 2022, 2023, 2024]

export function getHistoricalData(districtIds, metric = 'avgPsf') {
  return YEARS.map((year, i) => {
    const row = { year: year.toString() }
    districtIds.forEach(id => {
      if (metric === 'avgPsf') row[id] = psfBase[id]?.[i] ?? 0
      else if (metric === 'avgPrice') row[id] = Math.round(priceBase[id]?.[i] * 10) / 10
      else if (metric === 'volume') row[id] = volumeBase[id]?.[i] ?? 0
    })
    return row
  })
}

export function getDistrictStats(districtId) {
  const psf = psfBase[districtId] ?? [0,0,0,0,0]
  const price = priceBase[districtId] ?? [0,0,0,0,0]
  const vol = volumeBase[districtId] ?? [0,0,0,0,0]
  const avgPsf = psf[4]
  const minPsf = Math.min(...psf)
  const maxPsf = Math.max(...psf)
  const avgPrice = price[4]
  const minPrice = Math.min(...price)
  const maxPrice = Math.max(...price)
  const volume = vol[4]
  const yoy = Math.round(((psf[4] - psf[3]) / psf[3]) * 1000) / 10
  return { avgPsf, minPsf, maxPsf, avgPrice, minPrice, maxPrice, volume, yoy }
}

